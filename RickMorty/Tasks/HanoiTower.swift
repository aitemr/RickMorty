import Foundation

// MARK: - Задача 2: Ханойские башни
//
// Классическая рекурсивная головоломка.
// Есть 3 башни и n дисков разного размера на первой башне.
// Цель: переместить все диски на третью башню.
// Правило: больший диск нельзя класть на меньший.
// Минимальное количество ходов: 2^n - 1.

/// Решает головоломку «Ханойские башни» для `n` дисков.
///
/// Алгоритм (рекурсия):
/// 1. Переместить `n-1` дисков с исходной башни на вспомогательную
/// 2. Переместить диск `n` с исходной башни на целевую
/// 3. Переместить `n-1` дисков со вспомогательной башни на целевую
/// 4. Базовый случай: если дисков 0 — ничего не делать
///
/// Формат каждого хода: `"Диск a с башни b переложить в башню c"`
///
/// - Parameter n: Количество дисков (0 < n < 100).
/// - Returns: Массив строк с последовательностью перемещений.
func hanoiTower(_ n: Int) -> [String] {
    var moves: [String] = []

    func solve(_ disk: Int, from source: Int, to target: Int, via auxiliary: Int) {
        guard disk >= 1 else { return }
        solve(disk - 1, from: source, to: auxiliary, via: target)
        moves.append("Диск \(disk) с башни \(source) переложить в башню \(target)")
        solve(disk - 1, from: auxiliary, to: target, via: source)
    }

    solve(n, from: 1, to: 3, via: 2)
    return moves
}

/// Печатает решение головоломки для `n` дисков в консоль.
func printHanoiTower(_ n: Int) {
    for move in hanoiTower(n) {
        print(move)
    }
}
